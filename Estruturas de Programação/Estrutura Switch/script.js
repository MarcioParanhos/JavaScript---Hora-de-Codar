let chosenOption = prompt ("Escolha um numero de 1 a 3")

switch (chosenOption){
    case "1":
        alert("Você escolheu o 1")
        break
    case "2":
        alert("Você escolheu o 2")
        break
    case "3":
        alert("Você escolheu o 3")
        break
    default :
        alert("Opção invalida")       
        break
}